export const logout = createAsyncThunk('auth/logout', async () => {
  try{
      return await authService.logout();
  } catch (error) { 
      // return thunkAPI.rejectWithValue("Error with registration");
  }
});


export const authSlice = createSlice({
  name: 'auth',
  initialState,
  // The `reducers` field lets us define reducers and generate associated actions
  reducers: {
    reset: (state) => {
      state.message = '';
      state.registerSuccess = false;
      state.loginSuccess = false;
      state.isLoading = false;
      state.isError = false;
    },
  },
  // The `extraReducers` field lets the slice handle actions defined elsewhere,
  // including actions generated by createAsyncThunk or in other slices.
  extraReducers: (builder) => {
    builder
      // register
      .addCase(register.pending, state => {
        state.isLoading = true;
      })
      .addCase(register.fulfilled, (state, action) => {
          state.isLoading = false;
          state.registerSuccess = true;
          state.message = action.payload;
      })
      .addCase(register.rejected, (state, action) => {
          state.isLoading = false;
          state.isError = true;
          state.message = action.payload;
          state.user = null;
      })

      // login cases
      .addCase(login.pending, state => {
          state.isLoading = true;
      })
      .addCase(login.fulfilled, (state, action) => {
          state.isLoading = false;
          state.loginSuccess = true;
          state.user = action.payload.user;
      })
      .addCase(login.rejected, (state, action) => {
          state.isLoading = false;
          state.isError = true;
          state.message = action.payload;
          state.user = null;
      })

      // logout case
      .addCase(logout.pending, state => {
          state.isLoading = true;
      })
      .addCase(logout.fulfilled, state => {
          state.isLoading = false;
          state.logoutSuccess = true;
          state.user = null;
          state.message = "Logout Successful";
      })
      .addCase(logout.rejected, state => {
          state.isLoading = false;
          state.isError = true;
          state.message = "Failed to log out";
          state.user = null;
      })
  },
});

export const { reset } = authSlice.actions;

// The function below is called a selector and allows us to select a value from
// the state. Selectors can also be defined inline where they're used instead of
// in the slice file. For example: `useSelector((state: RootState) => state.user.value)`
export const selectUser = (state) => state.auth.user;
export const selectMessage = (state) => state.auth.message;
export const selectLoginSuccess = (state) => state.auth.loginSuccess;
export const selectRegisterSuccess = (state) => state.auth.registerSuccess;
export const selectIsLoading = (state) => state.auth.isLoading;
export const selectIsError = (state) => state.auth.isError;

export default authSlice.reducer;
